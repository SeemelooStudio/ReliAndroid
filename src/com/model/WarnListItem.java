package com.model;

public class WarnListItem {

	private String warn_id;
	private String warn_title;
	private String warn_content;
	private String warn_date;
	private String warn_other;
	
	public String getWarn_id() {
		return warn_id;
	}
	public void setWarn_id(String warn_id) {
		this.warn_id = warn_id;
	}
	public String getWarn_title() {
		return warn_title;
	}
	public void setWarn_title(String warn_title) {
		this.warn_title = warn_title;
	}
	public String getWarn_content() {
		return warn_content;
	}
	public void setWarn_content(String warn_content) {
		this.warn_content = warn_content;
	}
	public String getWarn_date() {
		return warn_date;
	}
	public void setWarn_date(String warn_date) {
		this.warn_date = warn_date;
	}
	public String getWarn_other() {
		return warn_other;
	}
	public void setWarn_other(String warn_other) {
		this.warn_other = warn_other;
	}
}
