package com.model;

public class DailyDetailItem {

	private String fct_name;
	private String is_join;
	private String all_num;
	private String on_num;
	
	public String getFct_name() {
		return fct_name;
	}
	public void setFct_name(String fct_name) {
		this.fct_name = fct_name;
	}
	public String getIs_join() {
		return is_join;
	}
	public void setIs_join(String is_join) {
		this.is_join = is_join;
	}
	public String getAll_num() {
		return all_num;
	}
	public void setAll_num(String all_num) {
		this.all_num = all_num;
	}
	public String getOn_num() {
		return on_num;
	}
	public void setOn_num(String on_num) {
		this.on_num = on_num;
	}
}
